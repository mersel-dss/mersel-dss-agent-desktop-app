interface WordmarkProps {
  className?: string;
  /** Logotype yüksekliği (px). */
  height?: number;
  /** "İmzamatik" ürün ekini gizle (yalnız resmî "mersel" logosu). */
  productless?: boolean;
  /** Ürün ekine ek sınıf (ör. dar ekranda gizlemek için). */
  suffixClassName?: string;
}

/** Resmî "mersel" logosu + ürün eki → "mersel İmzamatik". */
export function MerselWordmark({
  className,
  height = 18,
  productless = false,
  suffixClassName,
}: WordmarkProps) {
  return (
    <span className={cn("inline-flex items-center gap-1.5", className)}>
      <MerselLogotype style={{ height }} />
      {productless ? null : (
        <span
          className={cn(
            "whitespace-nowrap font-semibold tracking-tight text-fg",
            suffixClassName,
          )}
          // Punto, logodaki "mersel" yazısının kapak yüksekliğine (≈0.59·H)
          // eşlenir; optik merkez hizası için kapsayıcı items-center'dır.
          style={{ fontSize: Math.round(height * 0.82), lineHeight: 1 }}
        >
          İmzamatik
        </span>
      )}
    </span>
  );
}
